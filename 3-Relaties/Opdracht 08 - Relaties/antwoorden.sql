# Antwoorden

## One to One
1. PC heeft maar 1 gebruiker en een gebruiker heeft maar 1 PC
2. Fiets heeft 1 gebruiker en een gebruiker heeft 1 fiets
3. Een persoon heeft 1 bed en de bed heeft 1 persoon

## One to Many
1. Een persoon heeft veel kleding en de kleding heeft maar 1 persoon
2. Een persoon heeft 1 achternaam en meerdere mensen hebben de achternaam.
3. Student heeft in 1 lokaal les en er zitten meerdere studenten in een lokaal
   
## Many to Many
1. Ik heb meerdere vrienden en vrienden hebben ook meerdere vrienden
2. Student heeft meerdere docenten en docenten hebben meerdere studenten.
3. Een gamer speelt meerdere games en games worden gespeelt door meerdere gamers.